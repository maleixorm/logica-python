# variável de string
nome = "Marcos";

# exibindo informações na tela
print("Olá mundo!");
print('Seja bem-vindo', nome);

# variaveis numéricas
num1 = 11;
num2 = 5;

# soma
soma = num1 + num2;
# subtração
subtr = num1 - num2;
# multiplicação
multi = num1 * num2;
# divisão
div = num1 / num2;
# resto da divisão
mod = num1 % num2;

print(soma, subtr, multi, div, mod);