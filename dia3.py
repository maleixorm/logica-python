# operadores aritméticos

a = 5
b = 2

print(a + b) # soma
print(a - b) # divisão
print(a * b) # multiplicação
print(a / b) # divisao
print(a // b) # divisão inteira
print(a % b) # resto da divisao
print(a ** b) # exponenciação


# Operadores de Comparação

x = 10
y = 5

print(x > 5)
print(x >= 5)
print(y < 5)
print(y <= 5)
print(x != y)